//program to print factorial for number 2 to 7
public class fact {
    public static void main(String[] args) {
        int fact=1;
        for(int i=2;i<=7;i++)   
        {
            for(i=2;i<=7;i++){
                fact = fact * i;
                System.out.println("factorial of " + i + " is " + fact);
            }
    
        }
    }
    
}
